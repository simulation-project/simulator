    public class LA1Verifier extends InputVerifier {

        public boolean verify(JComponent input) {
            la_lbl.setText("");
            Boolean check = true;
            JTextField tf = (JTextField) input;
            String s = tf.getText();

            if (!Pattern.matches("\\d*$", s)) {
                la_lbl.setText("must be numeric");
                check = false;
            } else if (s.length() != 3) {
                la_lbl.setText("must be 3 digits");
                check = false;
            }

            return check;
        }
    }

    public class LA2Verifier extends InputVerifier {

        public boolean verify(JComponent input) {
            la_lbl.setText("");
            Boolean check = true;
            JTextField tf = (JTextField) input;
            String s = tf.getText();

            if (!Pattern.matches("\\d*$", s)) {
                la_lbl.setText("must be numeric");
                check = false;
            } else if (s.length() != 2) {
                la_lbl.setText("must be 2 digits");
                check = false;
            }

            return check;
        }
    }

    public class LA3Verifier extends InputVerifier {

        public boolean verify(JComponent input) {
            la_lbl.setText("");
            Boolean check = true;
            JTextField tf = (JTextField) input;
            String s = tf.getText();

            if (!Pattern.matches("\\d*$", s)) {
                la_lbl.setText("must be numeric");
                check = false;
            } else if (Integer.parseInt(s) >= 65535) {    //65535
                la_lbl.setText("must be less than 65535");
                check = false;
            }

            return check;
        }
    }



