    public class NameVerifier extends InputVerifier {

        public boolean verify(JComponent input) {
            name_lbl.setText("");
            Boolean check = true;
            JTextField tf = (JTextField) input;
            String s = tf.getText();
            if (s.length() == 0) {
                //JOptionPane.showMessageDialog( null , "Name is empty ");
                //name_lbl.setVisible(true);
                name_lbl.setText("Name is empty");
                check = false;
            } else if (s.length() > 7) {
                //JOptionPane.showMessageDialog(null, "Name must be less than 7 digits ");
                name_lbl.setText("1-7 digits are required");
                check = false;
            }
            return check;
        }
    }

    public class NIVerifier extends InputVerifier {

        public boolean verify(JComponent input) {
            ni_lbl.setText("");
            Boolean check = true;
            JTextField tf = (JTextField) input;
            String s = tf.getText();

            if (!Pattern.matches("\\d*$", s)) {
                ni_lbl.setText("NI isn't numeric");
                check = false;
            }
            if (s.length() == 0) {
                //JOptionPane.showMessageDialog( null , "Name is empty ");
                ni_lbl.setText("NI is empty");
                check = false;
            } else if (!s.equals("0") && !s.equals("2") && !s.equals("3")) {
                //JOptionPane.showMessageDialog( null , "Name must be less than 3 digits ");
                ni_lbl.setText("0 or 2 or 3 is required");
                check = false;
            }

            return check;
        }
    }

    public class SPCVerifier extends InputVerifier {

        public boolean verify(JComponent input) {
            ni_lbl.setText("");
            Boolean check = true;
            JTextField tf = (JTextField) input;
            
            String s = tf.getText();
            int num = Integer.parseInt(s);
            if (!Pattern.matches("\\d*$", s)) {
                ni_lbl.setText("SPC isn't numeric");
                check = false;
            }
            if (s.length() == 0) {
                //JOptionPane.showMessageDialog( null , "Name is empty ");
                ni_lbl.setText("SPC is empty");
                check = false;
            } else if (num < 1 || num > 65535) {
                //JOptionPane.showMessageDialog( null , "Name must be less than 14 digits ");
                ni_lbl.setText("must be in range 1 - 65535");
                check = false;
            }

            return check;
        }
    }

